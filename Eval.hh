const int piece_value[6] = {100,310,330,500,900,10000};

unsigned int gamestate(Board bd) // Gamestate calculator (0 is start, higher is later)
{
    int piececount = count_bits(bd.all());
    int total_value = 0;
    for (int i = 0; i < 5; i++)
    {
        total_value += piece_value[i] * (count_bits(bd.black[i]) + count_bits(bd.white[i]));
    }
    return 0;
}

int eval(Board* bd)
{
    int total = 0;
    for (int i = 0; i < 64; i++)
    {
        uint64_t pos = 1ULL << i;
        for (int pc = 0; pc < 6; pc++)
        {
            if (pos & bd->white[pc]) {total += piece_value[pc] + piece_square_value[pc][i];}

            else if (pos & bd->black[pc]) {total -= piece_value[pc] + piece_square_value[pc][63-i];}

        }
    }
    return total;
}

